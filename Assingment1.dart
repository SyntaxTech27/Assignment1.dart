// Dart program demonstrating data types

void main() {
  // Example of int data type
  int age = 25;

  // Example of double data type
  double weight = 65.5;

  // Example of String data type
  String name = 'John Doe';

  // Example of List data type
  List<String> fruits = ['Apple', 'Banana', 'Orange'];

  // Example of Map data type
  Map<String, int> studentGrades = {
    'Alice': 85,
    'Bob': 72,
    'Charlie': 90
  };

  // Printing out the values
  print('Age: $age'); // Printing int
  print('Weight: $weight'); // Printing double
  print('Name: $name'); // Printing String
  print('Fruits: $fruits'); // Printing List
  print('Student Grades: $studentGrades'); // Printing Map
}
